package sample.data;

public class Level3Original {
	private Float id;
	private String name;
	
	public Level3Original() {
	}
	
	public Level3Original(Float id, String name) {
		this.id = id;
		this.name = name;
	}
	
	public Float getId() {
		return id;
	}
	public void setId(Float id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
